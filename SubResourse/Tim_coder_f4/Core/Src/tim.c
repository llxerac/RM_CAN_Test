extern uint8_t ds_fm;
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)//中断回调函数
{
  /*防止未使用参数编译警告*/
	UNUSED(htim);
 
	if(htim->Instance == htim9.Instance)  //TIMER4
	{
		if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  //CHANNEL_1
		{
			if(Edge == 0)          //捕获上升沿
			{
				RisingTime = HAL_TIM_ReadCapturedValue(&htim9, TIM_CHANNEL_1);    //获取上升沿时间点
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim9, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);   //切换捕获极性 下降沿
				HAL_TIM_IC_Start_IT(&htim9, TIM_CHANNEL_1);     //切换后重启中断
 
				Edge = 1;          	 //捕获极性标值换为下降沿
			}
			else if(Edge == 1)     //捕获下降沿
			{
				FallingTime = HAL_TIM_ReadCapturedValue(&htim9, TIM_CHANNEL_1);    //获取下降沿时间点
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim9, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);     //切换捕获极性上升沿
				HAL_TIM_IC_Start_IT(&htim9, TIM_CHANNEL_1);   	 //切换捕获极性后重启中断
 
				HighTime = FallingTime < RisingTime ? FallingTime + 0xffff - RisingTime + 1 : FallingTime - RisingTime;
				//高电平时间=下降沿时间 - 上升沿时间(若下降沿时间<上升沿时间FallingTime + 0xffff - RisingTime + 1)
				dis_fm = HighTime * 17/ 100; //距离计算 HighTime*（1/100000）秒*34000厘米/2
				ds_fm = dis_fm;
				printf("距离 = %dcm \r\n", dis_fm);
			
				Edge = 0;		  			 //一次采集完毕，捕获上升沿标志
			}
		}
	}
}

